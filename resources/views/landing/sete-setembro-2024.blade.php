                                    <p>Não esquecendo de que o que ocorria em Portugal, ainda refletia não só na economia, mas principalmente na política do Brasil. Consequentemente, A Revolução do Porto ou Revolução Liberal, exigia o retorno do rei e sua corte para Lisboa e naqueles dias de 1820, que calcava suas bases em uma nova constituição que seguia os caminhos das monarquias pós napoleônicas, acabando com o absolutismo.</p>
                                    <p>Para não desobedecer ao parlamento e à burguesia portuguesa, Dom João VI teve que voltar para Portugal, mas ao contrário do que Lisboa pretendia, não devolveu o Brasil à condição de colônia, mantendo-nos como parte integrante do reino.</p>
